const getMe = async (token?: string): Promise<Consultant> => {
  if (!token) {
    return mockConsultant;
  }
  const response = await fetch("https://api.prd.aws.netlight.com/me", {
    headers: { authorization: `Bearer ${token}` },
  });
  const me = await response.json();
  return me;
};

